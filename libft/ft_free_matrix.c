/* **************************** [V] INCLUDES [V] **************************** */
#include <stdlib.h> /*
#   void free(void *);
*/
/* **************************** [^] INCLUDES [^] **************************** */

void
	ft_free_matrix(char **matrix)
{
	register int	y;

	y = -1;
	while (++y, matrix[y])
	{
		free(matrix[y]);
		matrix[y] = ((void *)0);
	}
	free(matrix);
	matrix = ((void *)0);
}
